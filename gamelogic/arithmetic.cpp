#include "arithmetic.h"


int add(int a, int b){
		int result;
		result = a+b;
		return result;
}

int sub(int a, int b){
		int result;
		result = a-b;
		return result;
}

int multi(int a, int b){
		int result;
		result = a*b;
		return result;
}

float div(int a, int b){ 
		float result;
		result = a/b;
		return result;
}
